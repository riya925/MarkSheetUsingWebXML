package bean;

public class Student 
{
	int roll;
	String name;
	
	public Student()
	{
		roll=19;
		name="Priyanka";
	}

	public int getRoll() 
	{
		return roll;
	}

	public void setRoll(int roll) 
	{
		this.roll = roll;
	}

	public String getName() 
	{
		return name;
	}

	public void setName(String name) 
	{
		this.name = name;
	}
	
}
